package MODEL;

public class pruebTabla {

    private String cosa1;
    private String cosa2;

    public pruebTabla(String cosa1, String cosa2) {
        this.cosa1 = cosa1;
        this.cosa2 = cosa2;
    }

    public String getCosa1() {
        return cosa1;
    }

    public void setCosa1(String cosa1) {
        this.cosa1 = cosa1;
    }

    public String getCosa2() {
        return cosa2;
    }

    public void setCosa2(String cosa2) {
        this.cosa2 = cosa2;
    }
}
